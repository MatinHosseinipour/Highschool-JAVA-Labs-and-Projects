    void setYear(String cors){
        jPanel4.setEnabled(true);
        jRadioButton1.setEnabled(true);
        jRadioButton2.setEnabled(true);
        course = cors;
    }
    void setGrade(int grad){
        jPanel1.setEnabled(true);
        jComboBox1.setEnabled(true);
        grade = grad;
        setCourse();
    }
    void setCourse(){
        switch (grade){
                case 9:
                    removeCourses();
                    jComboBox1.addItem("English");
                    jComboBox1.addItem("Mathematics");
                    break;
                case 10:
                    removeCourses();
                    jComboBox1.addItem("English");
                    jComboBox1.addItem("Mathematics");
                    break;
                case 11:
                    removeCourses();
                    jComboBox1.addItem("English");
                    jComboBox1.addItem("Computer Science");
                    jComboBox1.addItem("Mathematics");
                    break;
                case 12:
                    removeCourses();
                    jComboBox1.addItem("English");
                    jComboBox1.addItem("Mathematics");
                    jComboBox1.addItem("Computer Science");
                    break;
                default:
                    break;
        }
    }
    void removeCourses(){
        try {
            jComboBox1.removeAllItems();
            jComboBox1.addItem("Our Courses");
        } catch (Exception e) {
        }
    }
    void showInformation(){
        switch (course) {
            case "Computer Science":
                jTextArea1.setText(ICS());
                break;
            case "Mathematics":
                jTextArea1.setText(Math());
                break;
            case "English":
                jTextArea1.setText(English());
                break;
            default:
                bug();
                break;
                
        }
    }
